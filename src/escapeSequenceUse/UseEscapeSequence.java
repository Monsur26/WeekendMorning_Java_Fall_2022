package escapeSequenceUse;

public class UseEscapeSequence {
    public static void main(String[] args) {
        System.out.println("Hello \" World \""); //Double Quote
        System.out.println("Hello \' World \'"); // Single Quote
        System.out.println("Hello  World\b  "); // backspace
        System.out.println("Hello \t World  "); // Tab
        System.out.println("I eat rice. \nI sleep a lot. \nI like going outside.  "); // new lines
    }
}
