package controlflow;

public class UseWhileLoop {

    public void whileLoop(int num){

        while(num<=10){
            System.out.println("The Number is : "+num);
            num++;
        }
    }

    public void reservseWhileLoop(int num){
        while (num>=0){
            System.out.println(num);
            num--;
        }
    }


}
